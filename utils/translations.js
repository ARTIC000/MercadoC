// Función para cambiar símbolo de yen (¥) a peso ($)
const replaceYenWithPeso = text => {
  return typeof text === 'string' ? text.replace(/¥/g, '$') : text
}

// Función principal de traducción
export const translateProductName = name => {
  if (!name || typeof name !== 'string') return name || ''

  // Primero reemplazamos los símbolos de yen
  name = replaceYenWithPeso(name)

  // Si el texto ya está en español (solo caracteres latinos)
  if (/^[\w\sáéíóúÁÉÍÓÚñÑ,.;:¡!¿?\-()]+$/.test(name)) {
    return name
  }

  // Buscar traducción exacta
  if (productTranslations[name]) {
    return replaceYenWithPeso(productTranslations[name])
  }

  // Buscar coincidencia insensible a mayúsculas/minúsculas
  const lowerName = name.toLowerCase().trim()
  const foundKey = Object.keys(productTranslations).find(k => k.toLowerCase() === lowerName)

  // Si no se encuentra, buscar coincidencia parcial
  if (!foundKey) {
    for (const [key, value] of Object.entries(productTranslations)) {
      const keyLower = key.toLowerCase()
      if (name.toLowerCase().includes(keyLower) || keyLower.includes(lowerName)) {
        return replaceYenWithPeso(value)
      }
    }
  }

  return foundKey ? replaceYenWithPeso(productTranslations[foundKey]) : name
}

// Función para traducción profunda (objetos/arrays)
export const translateDeep = value => {
  if (Array.isArray(value)) {
    return value.map(translateDeep)
  }
  if (value && typeof value === 'object') {
    return Object.entries(value).reduce((acc, [k, v]) => {
      acc[k] = translateDeep(v)
      return acc
    }, {})
  }
  if (typeof value === 'string') {
    return translateProductName(value)
  }
  return value
}

// Función para traducción simple de objetos (alternativa)
export const translateObject = obj => translateDeep(obj)
